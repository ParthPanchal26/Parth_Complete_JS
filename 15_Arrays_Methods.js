// Array toString.
let arr1 = [1,2,3,4,5] // toString() convers an Array to a string type
let arr_str = arr1.toString();
console.log(arr1);

// Array Join.
let arr2 = arr1.join(" and "); // It joins value to each element in array
console.log(arr2);

// Array Push.
let arr3 = arr1.push(6); // It add element at end of array
console.log(arr1, arr3);

// Array pop.
let arr4 = arr1.pop(); // It Removes last element of array
console.log(arr1, arr4);

// Array unshift
let arr5 = arr1.unshift(0); // It adds element at starting of array
console.log(arr1, arr5);

// Array shift 
let arr6 = arr1.shift(); // It Removes element at ending of array
console.log(arr1, arr5);

